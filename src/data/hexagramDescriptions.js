/**
 * Tra cứu mô tả quẻ theo key
 * @param {number} thuongId - số thượng quái (1–8)
 * @param {number} haId     - số hạ quái (1–8)
 * @returns {Object|null}
 */
export function getHexagramDescription(thuongId, haId) {
  if (!thuongId || !haId) return null;
  return HEXAGRAM_DESCRIPTIONS[`${thuongId}-${haId}`] || null;
}

